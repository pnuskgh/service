funcUsing() {
    info "Using : managectl.bash COMMAND OPTIONS"
    info "        COMMAND              : 실행할 명령"
    info "        OPTIONS              : 명령 옵션"

    info "    COMMAND"

    info " "
    exit 1
}

### ------------------------------------------------------------------------------------------------
###     Main, 2016.1.27 ~ 2016.1.27, Version 0.00.001
### ------------------------------------------------------------------------------------------------
###---  Command Line에서 입력된 인수를 검사한다.
if [[ 0 < $# ]]; then
    COMMAND=$1
    shift
    OPTIONS=$*
else
    funcUsing
fi

case ${COMMAND} in
    *)
        funcUsing
        ;;
esac
exit 0

