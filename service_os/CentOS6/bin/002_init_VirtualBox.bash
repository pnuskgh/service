### ------------------------------------------------------------------------------------------------
###     Network 초기화
###         로그인이 안되거나 공인망에 접속이 안될 경우에 실행 한다.
### ------------------------------------------------------------------------------------------------
cd /etc/sysconfig/network-scripts
ifdown  ifcfg-eth0
ifup    ifcfg-eth0
ping -c 3 google.com

### ------------------------------------------------------------------------------------------------
###     Image 작성을 위한 환경 구성
### ------------------------------------------------------------------------------------------------
mkdir -p /data/images/CentOS6
cd /data/images/CentOS6

echo " "
echo "/data/images/CentOS6/ 폴더에 CentOS 6 이미지 작성을 위한 환경을 설정 하세요."
