###     VirtualBox 구성 정보
###         CentOS6_64 (1 core / 2048MB memory / 8GB storage)
###         Network : NAT, 호스트 전용 어댑터 (192.168.56.102)
###         Hostname : localhost.jopenbusiness.com
###         Storage 구성 (/dev/sda, ext4)
###             /dev/sda1, /dev/sda 상에 부트로더 설치, SWAP 없음
###         Minimal 구성으로 설치
