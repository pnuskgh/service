### ------------------------------------------------------------------------------------------------
###     File backup
### ------------------------------------------------------------------------------------------------
backup() {
    local SOURCE=$1
    local FILENAME=$2

    if [[ "z${BACKUP_DIR}z" == "zz" ]]; then
        return 1
    fi

    if [[ "z${TIMESTAMP}z" == "zz" ]]; then
        /usr/bin/cp -f ${SOURCE}/${FILENAME} ${BACKUP_DIR}/${FILENAME}
    else
        cp ${SOURCE}/${FILENAME} ${BACKUP_DIR}/${FILENAME}_${TIMESTAMP}
    fi
    return 0
}

